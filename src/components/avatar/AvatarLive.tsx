/* ── Profile icon component ──────────────────────────────────────────────── */
function ProfileIcon({ size, color }: { size: AvatarSize; color: string }) {
  const d = SIZE_PX[size];
  return (
    <svg
      width={d}
      height={d}
      viewBox={`0 0 ${d} ${d}`}
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      style={{ position: 'absolute', top: 0, left: 0 }}
      aria-hidden="true"
    >
      <path d={ICON_PATHS[size]} fill={color} />
    </svg>
  );
}

/* ── Main component ──────────────────────────────────────────────────────── */
export function AvatarLive({
  size = 'Medium',
  type = 'Initials',
  avatarStyle = 'Brand',
  initials = 'WC',
  imageSrc,
  brand: _brand = 'dragonpass',
}: AvatarLiveProps) {
  const d = SIZE_PX[size];

  /* Photo type always renders as Brand (no Neutral Photo variant in Figma) */
  const effectiveStyle = type === 'Photo' ? 'Brand' : avatarStyle;

  const bg = effectiveStyle === 'Brand' ? BRAND_BG : NEUTRAL_BG;
  const fg = effectiveStyle === 'Brand' ? BRAND_FG : NEUTRAL_FG;

  const ariaLabel =
    type === 'Initials'
      ? `Avatar ${initials.slice(0, 2).toUpperCase()}`
      : type === 'Photo'
        ? 'User photo'
        : 'User avatar';

  return (
    <div
      role="img"
      aria-label={ariaLabel}
      style={{
        position: 'relative',
        width: `${d}px`,
        height: `${d}px`,
        borderRadius: '50%',
        backgroundColor: type === 'Photo' ? 'transparent' : bg,
        overflow: 'hidden',
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        flexShrink: 0,
      }}
    >
      {/* ── Initials ── */}
      {type === 'Initials' && (
        <span
          style={{
            fontSize: `${FONT_PX[size]}px`,
            fontWeight: 600,
            color: fg,
            lineHeight: 1,
            letterSpacing: '0.02em',
            fontFamily: 'var(--atom-font-body, Poppins, sans-serif)',
            userSelect: 'none',
          }}
        >
          {initials.slice(0, 2).toUpperCase()}
        </span>
      )}

      {/* ── Icon (profile silhouette) ── */}
      {type === 'Icon' && <ProfileIcon size={size} color={fg} />}

      {/* ── Photo ── */}
      {type === 'Photo' && imageSrc && (
        <img
          src={imageSrc}
          alt=""
          style={{
            width: '100%',
            height: '100%',
            objectFit: 'cover',
            borderRadius: '50%',
          }}
        />
      )}
      {type === 'Photo' && !imageSrc && (
        /* Fallback: shows accent bg with initials icon when no image */
        <div
          style={{
            width: '100%',
            height: '100%',
            borderRadius: '50%',
            backgroundColor: BRAND_BG,
            position: 'relative',
          }}
        >
          <ProfileIcon size={size} color={BRAND_FG} />
        </div>
      )}
    </div>
  );
}
